<?php
class Estudante 
{
    //Atributos
    private $cpf;
    private $nome;
    private $curso;
    private $idade;

    //MÃ©todos GETs e SETs

    

    /**
     * Get the value of cpf
     */ 
    public function getCpf()
    {
        return $this->cpf;
    }

